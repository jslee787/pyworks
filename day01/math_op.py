#산술 연산자
print("10 + 4 =",10+4)
print("10 - 4 =",10-4)
print("10 * 4 =",10*4)
print("10 / 4 =",10/4)
print("10 // 4 =",10//4)    # // 은 몫
print("10 % 4 =",10%4)      # % 는 나머지
print("10 ** 4 =",10**4)    #**은 제곱

#도형의 면적
#1. 한 변의 길이가 5인 정사각형의 넓이

size = 5
area1 = size * size
print("정사각형의 넓이:", area1)

#2. 한 변의 길이가 5이고 높이가 7인 삼각형의 넓이
width = 5
height = 7
area2 = (width * height) / 2
print("삼각형의 넓이 :", area2)
